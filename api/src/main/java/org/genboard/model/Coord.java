package org.genboard.model;

public class Coord {
	
	private Double x;
	
	private Double y;

	
	public Coord() {
		super();
	}


	public Double getX() {
		return x;
	}


	public void setX(Double x) {
		this.x = x;
	}


	public Double getY() {
		return y;
	}


	public void setY(Double y) {
		this.y = y;
	}

	
	

}
